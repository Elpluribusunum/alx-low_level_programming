/**
 * add - Addition
 * @a: Number
 * @b: No
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - Subtraction
 * @a: Number
 * @b: No
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - Multiplication
 * @a: Number
 * @b: No
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - Division
 * @a: Number
 * @b: No
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - Mod
 * @a: Number
 * @b: No
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
